package com.fitech.gznx.common;

public class CellInfo {
	private int row;
	private int col;
	private String value;
	private String formual;
	private int color;
	private String cellPid;
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public int getCol() {
		return col;
	}
	public void setCol(int col) {
		this.col = col;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getFormual() {
		return formual;
	}
	public void setFormual(String formual) {
		this.formual = formual;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public String getCellPid() {
		return cellPid;
	}
	public void setCellPid(String cellPid) {
		this.cellPid = cellPid;
	}
	
}
