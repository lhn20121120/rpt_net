package com.gather.down.reports;

public class DataRangeBean {

    private String rangeID="";    //数据范围id
    private String reageName="";  //数据范围名称
	/**
	 * @return Returns the rangeID.
	 */
	public String getRangeID() {
		return rangeID;
	}
	/**
	 * @param rangeID The rangeID to set.
	 */
	public void setRangeID(String rangeID) {
		this.rangeID = rangeID;
	}
	/**
	 * @return Returns the reageName.
	 */
	public String getReageName() {
		return reageName;
	}
	/**
	 * @param reageName The reageName to set.
	 */
	public void setReageName(String reageName) {
		this.reageName = reageName;
	}
    
}
